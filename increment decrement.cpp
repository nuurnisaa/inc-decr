#include <iostream>

using namespace std;

int main()
{
    int num1=30;
    
   
    cout<<num1++<<endl; //post inc
    cout<<num1<<endl;
    cout<<++num1<<endl; //pre inc
    cout<<num1+1<<endl;
   
    cout<<num1--<<endl;
    cout<<num1<<endl;
    cout<<--num1<<endl;
    cout<<num1-1<<endl;
  
    
    
    
    return 0;
}
